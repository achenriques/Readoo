class Book {
  constructor(bookId, title, author, description, review, bookLikes, date, coverUrl, userId, genreId) {
    this.bookId = bookId;
    this.title = title;
    this.author = author;
    this.description = description;
    this.review = review;
    this.bookLikes = bookLikes;
    this.date = date;
    this.coverUrl = coverUrl;
    this.userId = userId;
    this.genreId = genreId;
  }
}
